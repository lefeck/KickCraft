// parseSimpleTemplate performs simple variable substitution
func parseSimpleTemplate(template string, data map[string]interface{}) (*config.KickstartConfig, error) {
	result := template

	// Simple variable substitution
	for key, value := range data {
		placeholder := "{{ ." + key + " }}"
		result = strings.ReplaceAll(result, placeholder, toString(value))
	}

	// Parse the result
	return parseKickstart(result)
}

