// ConfScriptName is the filename of the repository configuration script.
const ConfScriptName = "config.sh"

// ConfScriptTemplate is the Go text/template used to generate config.sh.
// The template receives a ConfScriptData struct so it can render the
// list of downloaded RPM package names as `yum install -y` commands.
const ConfScriptTemplate = `#!/bin/bash
# config.sh — local offline repository setup for KickCraft ISO
# Generated automatically during ISO build; do not edit manually.
#
# This script is executed by the installer (Anaconda) during the
# %pre or %post section so that RPMs embedded in
# build/mnt/packages/ can be installed without network access.
#
# The RPMs in /mnt/packages/ are indexed by createrepo_c (run
# automatically by the ISO build script). This script registers
# that directory as a yum/dnf repo so that packages referenced
# in the Kickstart %packages section can be resolved offline.

set -euo pipefail

PACKAGES_DIR="/mnt/packages"
REPO_FILE="/etc/yum.repos.d/kickcraft-local.repo"
{{- range .Packages }}
{{- if . }}
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
{{- break }}
{{- end }}
{{- end }}

echo "[KickCraft] Setting up local offline repository..."
echo "[KickCraft] Packages directory: ${PACKAGES_DIR}"
{{- range .Packages }}
{{- if . }}
echo "[KickCraft] Script directory: ${SCRIPT_DIR}"
{{- break }}
{{- end }}
{{- end }}

# Verify packages directory exists and contains RPMs
if [[ ! -d "${PACKAGES_DIR}" ]]; then
    echo "[KickCraft] WARNING: packages directory not found at ${PACKAGES_DIR}"
    exit 0
fi

RPM_COUNT=$(find "${PACKAGES_DIR}" -name '*.rpm' 2>/dev/null | wc -l)
echo "[KickCraft] Found ${RPM_COUNT} RPM file(s)"

if [[ "${RPM_COUNT}" -eq 0 ]]; then
    echo "[KickCraft] No RPMs found in ${PACKAGES_DIR}, skipping repo setup"
    exit 0
fi

# Backup original repo files (unless already backed up by a previous run)
if [[ ! -d "/etc/yum.repos.d/.kickcraft-backup" ]]; then
    echo "[KickCraft] Backing up original repo files..."
    mkdir -p "/etc/yum.repos.d/.kickcraft-backup"
    cp -a /etc/yum.repos.d/*.repo "/etc/yum.repos.d/.kickcraft-backup/" 2>/dev/null || true
fi

# If createrepo has not been run yet, run it now
if [[ ! -f "${PACKAGES_DIR}/repodata/repomd.xml" ]]; then
    echo "[KickCraft] Running createrepo to index packages..."
    if command -v createrepo_c >/dev/null 2>&1; then
        createrepo_c "${PACKAGES_DIR}"
    elif command -v createrepo >/dev/null 2>&1; then
        createrepo "${PACKAGES_DIR}"
    else
        echo "[KickCraft] WARNING: createrepo not found, skipping indexing"
    fi
fi

# Write Kickstart-compatible repo definition
# This file is read by Anaconda's %pre/%post environment when
# installing from the ISO, making /mnt/packages/ visible as a repo.
cat > "${REPO_FILE}" << 'KICKSTARTCFG'
[kickcraft-local]
name=KickCraft Local Offline Repository
baseurl=file:///mnt/packages
enabled=1
gpgcheck=0
priority=1
module_hotfixes=1
KICKSTARTCFG

echo "[KickCraft] Repository configured: ${REPO_FILE}"
echo "[KickCraft] Repo definition:"
cat "${REPO_FILE}"

# Verify repo is usable
if command -v dnf >/dev/null 2>&1; then
    echo "[KickCraft] Verifying repo with dnf repolist..."
    dnf repolist kickcraft-local --quiet 2>/dev/null || echo "[KickCraft] dnf repolist check skipped"
elif command -v yum >/dev/null 2>&1; then
    echo "[KickCraft] Verifying repo with yum repolist..."
    yum repolist kickcraft-local --quiet 2>/dev/null || echo "[KickCraft] yum repolist check skipped"
fi

# Install downloaded packages
{{- range .Packages }}
{{- if . }}
if command -v dnf >/dev/null 2>&1; then
    echo "[KickCraft] Installing package: {{ . }}"
    dnf install -y --setopt=install_weak_deps=False "{{ . }}" || echo "[KickCraft] WARNING: failed to install {{ . }}"
elif command -v yum >/dev/null 2>&1; then
    echo "[KickCraft] Installing package: {{ . }}"
    yum install -y "{{ . }}" || echo "[KickCraft] WARNING: failed to install {{ . }}"
else
    echo "[KickCraft] WARNING: neither dnf nor yum found, cannot install packages"
fi
{{- end }}
{{- end }}

echo "[KickCraft] Local offline repository setup complete"
`

// ConfScriptData holds the data passed to ConfScriptTemplate.
type ConfScriptData struct {
	Packages []string // list of package names requested by the user
}

// WriteConfScript writes the config.sh script to build/mnt/script/config.sh.
// It renders ConfScriptTemplate with the provided package names so the
// script contains `yum/dnf install` commands for each downloaded package.
func WriteConfScript(scriptPath string, packages []string) error {
	if err := os.MkdirAll(filepath.Dir(scriptPath), 0755); err != nil {
		return err
	}
	tmpl, err := template.New("conf-sh").Parse(ConfScriptTemplate)
	if err != nil {
		return err
	}
	data := ConfScriptData{Packages: packages}
	f, err := os.Create(scriptPath)
	if err != nil {
		return err
	}
	defer f.Close()
	return tmpl.Execute(f, data)
}

// WriteConfScriptFromTemplate is a thin wrapper kept for backward compatibility
// (delegates to WriteConfScript with an empty package list).
func WriteConfScriptFromTemplate(scriptPath string) error {
	return WriteConfScript(scriptPath, nil)
}
